/**
 * The SecondService class serves as an entry point for the application.
 * It demonstrates a simple Java program that outputs text to the console.
 */
public class SecondService {
  
  /**
   * The main method is the starting point of the application.
   * It prints "Hello, World!" to the console twice.
   *
   * @param args Command-line arguments (not used in this program).
   */
  public static void main(String[] args) {
    // Print "Hello, World!" to the console for the first time
    System.out.println("Hello, World!");

    // Print "Hello, World!" to the console for the second time
    System.out.println("Hello, World!");
  }
}